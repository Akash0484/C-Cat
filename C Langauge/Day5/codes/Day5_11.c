#include<stdio.h>

int main( )
{
    // relational operator
    // if-else ( control statments, loops ) 
    // < , > , <= ,  >= , == , != ( not equal to )  
    // relation operator => True or False ( 1 or 0 )
    // True => 1 
    // False => 0
    printf("%d ",4>2);//T => 1 
    printf("%d ",4<2);//F => 0 
    printf("%d ",4!=2);//T => 1
    printf("%d ",4==2);//F => 0
    printf("%d ",4>=2);//T => 1 
    printf("%d ",4<=2);//F => 0     
    return 0; 
}
/*
        == ( relational opr)
        =  ( assignment opr)
        != ( relational opr )( not equal to )
*/